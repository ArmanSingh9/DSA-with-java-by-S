public class a4Assignment_operators {
    public static void main(String[] args) {
        int A=10;
        int B;
        B=A;
        A+=10;
        B-=5; // B=B-5
        System.out.println(A);
        System.out.println(B);

        int c=10;
        int d=20;
        c*=2;
        d/=2;
        System.out.println(c);
        System.out.println(d);
    }
    
}
