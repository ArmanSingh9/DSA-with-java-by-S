public class a3Logical_operators{
    public static void main(String[] args) {
        System.out.println((3>2) && (5>3));
        System.out.println((3>2) && (5<3));
        System.out.println((3>3) || (5>0));
        System.out.println((10<5) || (10>20));
        System.out.println(!(3>2));
        System.out.println(!(0>5));
    }
}